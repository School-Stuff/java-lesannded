public class Pindala {

    public static double roopkylikS(double a, double h) {
        double s = a * h;
        return s;
    }


    public static double ristkylikS(double a, double h) {
        double s = a * h;
        return s;
    }

    
    public static double kolmnunrkS(double a, double h) {
        double s = a * h / 2;
        return s;
    }
    
}
